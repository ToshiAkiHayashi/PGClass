     <h1><br/>淡水老街的位置</h1>
           淡水老街位於新北市淡水區中正路、重建街、清水街等一帶，鄰近淡水捷運站，由於交通方便，因此淡水老街每到假日總是人山人海，為全家出遊、三五朋友逛街的好地方，淡水老街也集美食、小吃、老街為一身，近年來淡水老街更因不少古董店及民藝品店進駐，也營造出民俗色彩與懷舊風味。<br/>
           
        <h1> 淡水老街食在好味</h1> 
		淡水老街歷史悠久，也發展出多樣的飲食風貌，緊鄰海岸的中正路上便有眾多海鮮餐廳林立，其中以價廉物美的海風餐廳最受歡迎；淡水老街小吃方面，淡水魚丸、魚酥、鐵蛋、阿給等最膾炙人口；淡水老街中正路兩旁林立著眾多魚丸、魚酥店，其中不乏歷史悠久的老店，如德裕魚丸、可口魚丸、味香魚丸、許義魚酥等；耐人尋味的鐵蛋則是反覆滷製，最後蛋白縮成薄薄的黑色韌皮，氣味香醇，阿婆鐵蛋已有30多年歷史，是淡水老街最知名的小吃之一；阿給的作法是油炸豆腐皮中塞滿粉絲，在糊上魚漿，蒸過後澆上特製的辣椒醬，便成了這道淡水老街風味特殊的小吃。
           
            <h1>淡水暮色譜戀曲<br/></h1>
         『黃昏的淡水河邊，越頭看過去，有一對好情侶，坐在河岸邊...』一首老歌，道出了淡水似乎是情侶們約會的絕佳去處，淡水海岸的暮色，黃昏時分落日染紅河面一景，早已被無數詩人畫家詠歎；渡船頭有渡輪來往淡水及對岸的八里，許多人登船只為享受蕩漾河上的一番樂趣，從船上清晰可見淡水河岸沼澤地大片叢生的紅樹林保護區。</p>
